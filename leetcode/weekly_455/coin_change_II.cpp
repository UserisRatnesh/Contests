int helper(int index, vector<int> &coins, int amount, vector<vector<int>> &dp) {
  if (amount == 0) {
    return 1;
  }

  if (index >= coins.size() || amount < 0) {
    return 0;
  }

  if (dp[index][amount] != -1) {
    return dp[index][amount];
  }

  int pick_not_move = helper(index, coins, amount - coins[index], dp);
  int only_move = helper(index + 1, coins, amount, dp);

  dp[index][amount] = pick_not_move + only_move;
  return dp[index][amount];
}

int change(int amount, vector<int> &coins) {
  vector<vector<int>> dp(coins.size(), vector<int>(amount + 1, -1));
  return helper(0, coins, amount, dp);
}

// Better approach
int change_better(int amount, vector<int> &coins) {

  // unsigned long long can be used since amount can never be negative
  vector<unsigned long long> dp(amount + 1, 0);
  dp[0] = 1;

  for (auto coin : coins) {
    for (int a = coin; a <= amount; a++) {
      dp[a] += dp[a - coin];
    }
  }

  return dp[amount];
}

