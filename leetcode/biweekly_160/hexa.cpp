string hexaTrigesimal(int n) {
  if (n == 0) {
    return "0";
  }
  string digits = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ";
  string result;
  while (n > 0) {
    result += digits[n % 36];
    n /= 36;
  }
  reverse(result.begin(), result.end());
  return result;
}

string hexaDecinal(int n) {
  string hex = "";
  hex += (n == 0 ? "0" : "");
  int temp = n;
  while (temp > 0) {
    int digit = temp % 16;
    if (digit < 10)
      hex += ('0' + digit);
    else
      hex += ('A' + digit - 10);
    temp /= 16;
  }
  reverse(hex.begin(), hex.end());

  return hex;
}
string concatHex36(int n) {

  int n2 = n * n;
  int n3 = n2 * n;

  string hexaD = hexaDecinal(n2);

  string hexaTri = hexaTrigesimal(n3);

  return hexaD + hexaTri;
}

