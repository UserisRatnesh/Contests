// TC: TLE
int findDepth(long long val) {
  int cnt = 0;
  while (val > 1) {
    val = set_bits(val);
    cnt++;
    if (cnt > 5) {
      return -1;
    }
  }
  return cnt;
}

vector<int> popcountDepth(vector<long long> &nums,
                          vector<vector<long long>> &queries) {

  int n = nums.size();
  vector<int> ans;
  vector<int> depthVec(n, 0);
  for (int i = 0; i < n; i++) {
    depthVec[i] = findDepth(nums[i]);
  }

  for (auto it : queries) {
    int type = it[0];
    if (type == 2) {
      int index = it[1];
      long long val = it[2];
      nums[index] = val;
      int depth = findDepth(val);
      depthVec[index] = depth;
    } else {
      int l = it[1];
      int r = it[2];
      int k = it[3];

      // l se r tak me kitna nums ka value hai jiske liye depth == k hai
      // count number of elements == k from l to r in depthVec
      int cnt = count(depthVec.begin() + l, depthVec.begin() + r + 1, k);
      ans.push_back(cnt);
    }
  }

  return ans;
}

